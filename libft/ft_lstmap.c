#include "libft.h"
#include <stddef.h>

t_list	*ft_lstmap(t_list *lst, t_lft_mapfn f, t_lft_delfn del)
{
	t_list	*head;
	t_list	*new;

	head = NULL;
	while (lst)
	{
		new = ft_lstnew(f(lst->content));
		if (!new)
		{
			ft_lstclear(&head, del);
			return (NULL);
		}
		ft_lstadd_back(&head, new);
		lst = lst->next;
	}
	return (head);
}
